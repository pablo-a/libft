#include "libft.h"
#include <stdlib.h>

void	ft_lstdelone(t_struct **alst, void (*del)(void *, size_t))
{
	if (*alst == NULL || del == NULL)
		return ;
	del((*alst)->content, (*alst)->content_size);
	free(*alst);
	*alst = NULL;
}
